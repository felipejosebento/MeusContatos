public class ContatoModel {
    private int idContato;
    private String nomeContato;
    private String sobrenomeContato;
    private String idadeContato;
    private String phoneContato;
    private String imageContado;

    public int getIdContato() {
        return idContato;
    }

    public void setIdContato(int idContato) {
        this.idContato = idContato;
    }

    public String getNomeContato() {
        return nomeContato;
    }

    public void setNomeContato(String nomeContato) {
        this.nomeContato = nomeContato;
    }

    public String getSobrenomeContato() {
        return sobrenomeContato;
    }

    public void setSobrenomeContato(String sobrenomeContato) {
        this.sobrenomeContato = sobrenomeContato;
    }

    public String getImageContado() {
        return imageContado;
    }

    public void setImageContado(String imageContado) {
        this.imageContado = imageContado;
    }

    public String getPhoneContato() {
        return phoneContato;
    }

    public void setPhoneContato(String phoneContato) {
        this.phoneContato = phoneContato;
    }

    public String getIdadeContato() {
        return idadeContato;
    }

    public void setIdadeContato(String idadeContato) {
        this.idadeContato = idadeContato;
    }
}
